class Stats {
    constructor(
        hp,
        attack,
        defense,
        specialAttack,
        specialDefense,
        speed
    ) {
        this._hp = hp;
        this._attack = attack;
        this._defense = defense;
        this._specialAttack = specialAttack;
        this._specialDefense = specialDefense;
        this._speed = speed;
    }
}